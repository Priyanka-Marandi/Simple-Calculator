    private void btnsymequalsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsymequalsActionPerformed
        NumEnter2=Double.parseDouble(jtxtresults.getText());
        if(op == "+")
        {
            result=NumEnter1 +NumEnter2;
            jtxtresults.setText(String.valueOf(result));
        }
        
        else if(op == "-")
        {
            result=NumEnter1 - NumEnter2;
            jtxtresults.setText(String.valueOf(result));
        }
        
        else if(op == "*")
        {
            result=NumEnter1 * NumEnter2;
            jtxtresults.setText(String.valueOf(result));
        }
        else if(op == "/")
        {
            result=NumEnter1 / NumEnter2;
            jtxtresults.setText(String.valueOf(result));
        }
    }//GEN-LAST:event_btnsymequalsActionPerformed

